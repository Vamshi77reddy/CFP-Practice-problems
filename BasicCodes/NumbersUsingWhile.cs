using System;
using System.Collections.Generic;
using System.Text;

namespace BasicCodes
{
    internal class NumbersUsingWhile
    {
        public void WhileLoop()
        {
            int i = 1;
            Console.WriteLine("Natural Numbers Numbers are");
            while (i <= 10)
            {
                Console.WriteLine(i);
                i++;
            }
        }
    }
}
